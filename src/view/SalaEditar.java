    public void iniciar(String id, int num, int cadeiras, int computadores,
            String detalhes, Boolean ativo, TipoSala tipoSala, 
            Departamento departamento, Bloco bloco) {
        tfId.setText(id);
        tfId.setEnabled(false);
        spinNumero.setValue(num);
        spinQuantidadeCadeiras.setValue(cadeiras);
        spinQuantidadeComputadores.setValue(computadores);
        taDetalhes.setText(detalhes);
        checkAtiva.setSelected(ativo);
        
        carregaComboBoxes(tipoSala, departamento, bloco);
    }

    public void editar() {
        try {
            String id = tfId.getText();
            int num = (int) spinNumero.getValue();
            int cadeiras = (int) spinQuantidadeCadeiras.getValue();
            int computadores = (int) spinQuantidadeComputadores.getValue();
            String detalhes = taDetalhes.getText();
            boolean ativa = checkAtiva.isSelected();
            TipoSala tipoSala = (TipoSala) cbTipoSala.getSelectedItem();
            Departamento departamento = (Departamento) cbDepartamento.getSelectedItem();
            Bloco bloco = (Bloco) cbBloco.getSelectedItem();

            ArrayList novaLista = new ArrayList();
            novaLista.add(id);
            novaLista.add(num);
            novaLista.add(cadeiras);
            novaLista.add(computadores);
            novaLista.add(detalhes);
            novaLista.add(ativa);
            novaLista.add(tipoSala);
            novaLista.add(departamento);
            novaLista.add(bloco);

            if (control.editarSala(novaLista)) {
                JOptionPane.showMessageDialog(this, "As alteraÃ§Ãµes foram salvas com sucesso!");
                this.setVisible(false);
                this.dispose();
                new SalaLista().setVisible(true);
            } 
        } catch (HeadlessException error) {
            System.err.println("Erro: " + error);
        }
    }

    private void carregaComboBoxes(TipoSala tipoSalaSelecionada,
            Departamento departamentoSelecionado, Bloco blocoSelecionado) {
        DepartamentoRecursoController controlDepartamento = new DepartamentoRecursoController();
        ArrayList lista = controlDepartamento.listarDepartamento();
        Iterator iterator = lista.iterator();
        while (iterator.hasNext()) {
            Departamento departamento = new Departamento(
                    (int) iterator.next(),//ID
                    (String) iterator.next(),//Nome
                    (String) iterator.next(),//Sigla
                    (boolean) iterator.next()//Ativo
            );
            cbDepartamento.addItem(departamento);
            if(departamento.equals(departamentoSelecionado)) {
                cbDepartamento.setSelectedItem(departamento);
            }
        }

        BlocoRecursoController controlBloco = new BlocoRecursoController();
        lista = controlBloco.listarBlocos();
        iterator = lista.iterator();
        while (iterator.hasNext()) {
            Bloco bloco = new Bloco(
                    (int) iterator.next(),//ID
                    (char) iterator.next(),//Letra
                    (String) iterator.next(),//Nome
                    (String) iterator.next(),//Descricao
                    (boolean) iterator.next()//Ativo
            );
            cbBloco.addItem(bloco);
            if (bloco.equals(blocoSelecionado)) {
                cbBloco.setSelectedItem(bloco);
            }
        }

        TipoSalaRecursoController controlTipoSala = new TipoSalaRecursoController();
        lista = controlTipoSala.listarTipoSala();
        iterator = lista.iterator();
        while (iterator.hasNext()) {
            TipoSala tipoSala = new TipoSala(
                    (int) iterator.next(),//ID
                    (String) iterator.next()//Nome
            );
            cbTipoSala.addItem(tipoSala);
            if (tipoSala.equals(tipoSalaSelecionada)) {
                cbTipoSala.setSelectedItem(tipoSala);
            }
        }

    }

